#include "3-calc.h"
/**
 * op_add - addition
 * @a: first num
 * @b: second num
 * Return: Addition
 */

int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtraction
 * @a: first num
 * @b: second num
 * Return: subtraction
 */

int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplication
 * @a: first num
 * @b: second num
 * Return: multiplication
 */

int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - division
 * @a: first num
 * @b: second num
 * Return: Division
 */

int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - remainder
 * @a: first num
 * @b: second num
 * Return: Remainder
 */

int op_mod(int a, int b)
{
	return (a % b);
}
